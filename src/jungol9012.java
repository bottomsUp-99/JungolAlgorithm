public class jungol9012 {
    public static void main(String[] args) {
        int r = 5;
        double pi = 3.140000;
        double circumference = 2 * pi * r;
        double area = pi * r * r;
        System.out.printf("원주 = 5 * 2 * 3.14000 = %.6f\n", circumference);
        System.out.printf("둘레 = 5 * 5 * 3.14000 = %.6f", area);
    }
}